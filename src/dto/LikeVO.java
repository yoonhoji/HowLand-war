package dto;

public class LikeVO {
	private int lseq;
	private int tseq;
	private int no;
	
	public int getLseq() {
		return lseq;
	}
	public LikeVO setLseq(int lseq) {
		this.lseq = lseq;
		return this;
	}
	public int getTseq() {
		return tseq;
	}
	public LikeVO setTseq(int tseq) {
		this.tseq = tseq;
		return this;
	}
	public int getNo() {
		return no;
	}
	public LikeVO setNo(int no) {
		this.no = no;
		return this;
	}
}
